

class Book{
    constructor() {

    }

    author;
    title;
    obra;
    literaryWork;
    bookPlaceAcquired;
    quantityPages;
    isGifted;
    price;
    coverType;
    isHardCover;


    getAuthor() {
        return this.author;
    }
    setAuthor(author) {
        this.author = author;
    }
    getTitle() {
        return this.title;
    }
    setTitle(title) {
        this.title = title;
    }
    getObra() {
        return this.obra;
    }
    setObra(obra) {
        this.obra = obra;
    }
    getLiteraryWork() {
        return this.literaryWork;
    }
    setLiteraryWork(literaryWork) {
        this.literaryWork = literaryWork;
    }
    getBookPlaceAcquired() {
        return this.bookPlaceAcquired;
    }
    setBookPlaceAcquired(bookPlaceAcquired) {
        this.bookPlaceAcquired = bookPlaceAcquired;
    }
    getQuantityPages() {
        return this.quantityPages;
    }
    setQuantityPages(quantityPages) {
        this.quantityPages = quantityPages;
    }
    getIsGifted() {
        return this.isGifted;
    }
    setIsGifted(isGifted) {
        this.isGifted = isGifted;
    }
    getPrice() {
        return this.price;
    }
    setPrice(price) {
        this.price = price;
    }
    getCoverType() {
        return this.coverType;
    }
    setCoverType(coverType) {
        this.coverType = coverType;
    }
    getIsHardCover() {
        return this.isHardCover;
    }
    setIsHardCover(isHardCover) {
        this.isHardCover = isHardCover;
    }

}